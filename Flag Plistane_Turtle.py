import turtle
t=turtle.Turtle()
turtle.setup(600,600)
t.penup()
t.goto(-200,-200)
t.pendown()
t.fillcolor("green")
t.begin_fill()
t.forward(400)
t.left(90)
t.forward(100)
t.left(90)
t.forward(400)
t.left(90)
t.forward(100)
t.end_fill()
t.penup()
t.goto(-200,-100)
t.pendown()
t.fillcolor("white")
t.begin_fill()
t.left(90)
t.forward(400)
t.left(90)
t.forward(100)
t.left(90)
t.forward(400)
t.left(90)
t.forward(100)
t.left(90)
t.end_fill()
t.penup()
t.goto(-200,0)
t.pendown()
t.fillcolor("black")
t.begin_fill()
t.forward(400)
t.left(90)
t.forward(100)
t.left(90)
t.forward(400)
t.left(90)
t.forward(100)
t.left(90)
t.end_fill()
t.penup()
t.goto(-200,-200)
t.pendown()
t.fillcolor("red")
t.begin_fill()
t.left(45)
t.forward(213)
t.left(90)
t.forward(213)
t.left(135)
t.forward(300)
t.end_fill()
turtle.hideturtle()
turtle.mainloop()
