import turtle
t = turtle.Turtle()
turtle.setup(800,600)
t.speed(10)
t.penup()
t.goto(-200,0)
t.fillcolor('green')
t.begin_fill()
t.pendown()
for i in range(2):
    t.forward(400)
    t.left(90)
    t.forward(200)
    t.left(90)
t.end_fill()

t.penup()
t.goto(0,40)
t.fillcolor("red")
t.begin_fill()
t.pendown()
t.circle(60)
t.end_fill()

t.penup()
t.goto(-200,0)
t.fillcolor("gray")
t.begin_fill()
t.pendown()
t.right(90)
for i in range(2):
    t.forward(200)
    t.right(90)
    t.forward(20)
    t.right(90)
    t.forward(200)
t.end_fill()

t.penup()
t.goto(-200,-200)
t.fillcolor("pink")
t.begin_fill()
t.pendown()
t.left(90)
t.forward(50)
t.right(90)
t.forward(40)
t.right(90)
t.forward(100)
t.right(90)
t.forward(40)
t.right(90)
t.forward(50)
t.end_fill()


turtle.mainloop()
